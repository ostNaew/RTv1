#include "vector.h"

t_matrix_4x4	matrix_scalar(t_matrix_4x4 m, double n)
{
	int		i;
	int		j;

	i = -1;
	while (++i < 4 && (j = -1))
		while (++j < 4)
			m.matrix[i][j] *= n;
	return (m);
}
